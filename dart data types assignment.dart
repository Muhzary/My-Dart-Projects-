void main() {
  // int data type
  int age = 25;
  print('Age: $age');

  // double data type
  double weight = 65.5;
  print('Weight: $weight');

  // String data type
  String name = 'Hamisi Ally';
  print('Name: $name');

  // List data type (dynamic)
  List<dynamic> fruits = ['Apple', 'Banana', 'Orange'];
  print('Fruits: $fruits');

  // Map data type
  Map<String, dynamic> person = {
    'name': 'Alice',
    'age': 30,
    'isStudent': true,
  };
  print('Person: $person');
}
